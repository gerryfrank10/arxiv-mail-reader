// Decode LaTeX character escapes to Unicode, e.g. G\"orkem → Görkem, \c{S} → Ş
const RULES: Array<[RegExp, string]> = [
  // ---- braced forms {\"x} ----
  [/\{\\"\{([aeiouAEIOUy])\}\}/g, '$1'], // nested braces – strip outer first

  // ---- umlaut \"x or \"{x} ----
  [/\\"\{a\}/g, 'ä'], [/\\"\{e\}/g, 'ë'], [/\\"\{i\}/g, 'ï'],
  [/\\"\{o\}/g, 'ö'], [/\\"\{u\}/g, 'ü'], [/\\"\{y\}/g, 'ÿ'],
  [/\\"\{A\}/g, 'Ä'], [/\\"\{E\}/g, 'Ë'], [/\\"\{I\}/g, 'Ï'],
  [/\\"\{O\}/g, 'Ö'], [/\\"\{U\}/g, 'Ü'],
  [/\\"a/g, 'ä'], [/\\"e/g, 'ë'], [/\\"i/g, 'ï'],
  [/\\"o/g, 'ö'], [/\\"u/g, 'ü'], [/\\"y/g, 'ÿ'],
  [/\\"A/g, 'Ä'], [/\\"E/g, 'Ë'], [/\\"I/g, 'Ï'],
  [/\\"O/g, 'Ö'], [/\\"U/g, 'Ü'],

  // ---- acute \'x ----
  [/\\'\{a\}/g, 'á'], [/\\'\{e\}/g, 'é'], [/\\'\{i\}/g, 'í'],
  [/\\'\{o\}/g, 'ó'], [/\\'\{u\}/g, 'ú'], [/\\'\{y\}/g, 'ý'],
  [/\\'\{A\}/g, 'Á'], [/\\'\{E\}/g, 'É'], [/\\'\{I\}/g, 'Í'],
  [/\\'\{O\}/g, 'Ó'], [/\\'\{U\}/g, 'Ú'], [/\\'\{Y\}/g, 'Ý'],
  [/\\'a/g, 'á'], [/\\'e/g, 'é'], [/\\'i/g, 'í'],
  [/\\'o/g, 'ó'], [/\\'u/g, 'ú'], [/\\'y/g, 'ý'],
  [/\\'A/g, 'Á'], [/\\'E/g, 'É'], [/\\'I/g, 'Í'],
  [/\\'O/g, 'Ó'], [/\\'U/g, 'Ú'], [/\\'Y/g, 'Ý'],

  // ---- grave \`x ----
  [/\\`\{a\}/g, 'à'], [/\\`\{e\}/g, 'è'], [/\\`\{i\}/g, 'ì'],
  [/\\`\{o\}/g, 'ò'], [/\\`\{u\}/g, 'ù'],
  [/\\`a/g, 'à'], [/\\`e/g, 'è'], [/\\`i/g, 'ì'],
  [/\\`o/g, 'ò'], [/\\`u/g, 'ù'],

  // ---- cedilla \c{x} ----
  [/\\c\{c\}/g, 'ç'], [/\\c\{C\}/g, 'Ç'],
  [/\\c\{s\}/g, 'ş'], [/\\c\{S\}/g, 'Ş'],
  [/\\c\{t\}/g, 'ţ'], [/\\c\{T\}/g, 'Ţ'],

  // ---- caron \v{x} ----
  [/\\v\{c\}/g, 'č'], [/\\v\{C\}/g, 'Č'],
  [/\\v\{s\}/g, 'š'], [/\\v\{S\}/g, 'Š'],
  [/\\v\{z\}/g, 'ž'], [/\\v\{Z\}/g, 'Ž'],
  [/\\v\{r\}/g, 'ř'], [/\\v\{R\}/g, 'Ř'],
  [/\\v\{n\}/g, 'ň'], [/\\v\{N\}/g, 'Ň'],
  [/\\v\{d\}/g, 'ď'], [/\\v\{D\}/g, 'Ď'],

  // ---- circumflex \^x ----
  [/\\\^\{a\}/g, 'â'], [/\\\^\{e\}/g, 'ê'], [/\\\^\{i\}/g, 'î'],
  [/\\\^\{o\}/g, 'ô'], [/\\\^\{u\}/g, 'û'],
  [/\\\^a/g, 'â'], [/\\\^e/g, 'ê'], [/\\\^i/g, 'î'],
  [/\\\^o/g, 'ô'], [/\\\^u/g, 'û'],

  // ---- tilde \~x ----
  [/\\~\{n\}/g, 'ñ'], [/\\~\{N\}/g, 'Ñ'],
  [/\\~\{a\}/g, 'ã'], [/\\~\{o\}/g, 'õ'],
  [/\\~n/g, 'ñ'], [/\\~N/g, 'Ñ'],

  // ---- dot above \.{x} ----
  [/\\\.\{z\}/g, 'ż'], [/\\\.\{Z\}/g, 'Ż'],

  // ---- breve \u{x} ----
  [/\\u\{a\}/g, 'ă'], [/\\u\{A\}/g, 'Ă'],

  // ---- special letters ----
  [/\{\\ss\}/g, 'ß'], [/\\ss\b/g, 'ß'],
  [/\\aa\b/g, 'å'], [/\\AA\b/g, 'Å'],
  [/\\ae\b/g, 'æ'], [/\\AE\b/g, 'Æ'],
  [/\\oe\b/g, 'œ'], [/\\OE\b/g, 'Œ'],
  [/\\o\b/g,  'ø'], [/\\O\b/g,  'Ø'],
  [/\\l\b/g,  'ł'], [/\\L\b/g,  'Ł'],

  // ---- strip remaining braces and backslashes before letters ----
  [/[{}]/g, ''],
];

export function decodeLatex(s: string): string {
  let r = s;
  for (const [pat, rep] of RULES) r = r.replace(pat, rep);
  return r;
}
